package com.sample.api.model;

public class YelpUser {

	private String id;
	private String profile_url;
	private String image_url;
	private String name;

	public String getId() {
		return id;
	}

	public String getProfile_url() {
		return profile_url;
	}

	public String getImage_url() {
		return image_url;
	}

	public String getName() {
		return name;
	}

}
